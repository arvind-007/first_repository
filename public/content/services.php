                     <h6>  search engine optimization <h6>
                         
                         Search engine optimization (SEO) is the process of improving the quality and quantity of website traffic to a website or a
                          web page from search engines.[1] SEO targets unpaid traffic (known as "natural" or "organic" results) rather than direct
                           traffic or paid traffic. Unpaid traffic may originate from different kinds of searches, including image search, video 
                           search, academic search,[2] news search, and industry-specific vertical search engines.

                         As an Internet marketing strategy, SEO considers how search engines work, the computer-programmed algorithms that dictate
                          search engine behavior, what people search for, the actual search terms or keywords typed into search engines, and which 
                          search engines are preferred by their targeted audience. SEO is performed because a website will receive more visitors
                           from a search engine when websites rank higher on the search engine results page (SERP). These visitors can then
                            potentially be converted into customers
