                <h4> Application software<h4>
                 
                An application program (app or application for short) is a computer program designed to carry out a specific task other than
                one relating to the operation of the computer itself,[1] typically to be used by end-users. Word processors, media players,
                and accounting software are examples. The collective noun refers to all applications collectively.[2] The other principal 
                classifications of software are system software, relating to the operation of the computer, and utility software ("utilities").

                Applications may be bundled with the computer and its system software or published separately and may be coded as proprietary,
                open-source,or projects.[3] The term "app" often refers to applications for mobile devices such as phones

                APPS AND KILLER APPS
                
                Main article: Killer application
                Some applications are available in versions for several different platforms; others only work on one and are thus called, for 
                example, a geography application for Microsoft Windows, or an Android application for education, or a Linux game. Sometimes a
                 new and popular application arises which only runs on one platform, increasing the desirability of that platform. This is called 
                 a killer application or killer app. For example, VisiCalc was the first modern spreadsheet software for the Apple II and helped 
                 sell the then-new personal computers into offices. For Blackberry it was their email software.

                In recent years, the shortened term "app" (coined in 1981 or earlier[7]) has become popular to refer to applications for mobile
                 devices such as smartphones and tablets, the shortened form matching their typically smaller scope compared to applications on
                  PCs. Even more recently, the shortened version is used for desktop application software as well.

